package com.example.android_itpmapp_homework.pojo;

public class TitleDataItem {

    private int id;
    private String title;

    public TitleDataItem(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
